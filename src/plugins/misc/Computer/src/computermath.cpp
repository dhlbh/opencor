double factorial(double pNb)
{
    double res = 1.0;

    while (pNb > 1.0)
        res *= pNb--;

    return res;
}

//==============================================================================

double arbitrary_log(double pNb, double pBase)
{
    return log(pNb)/log(pBase);
}

